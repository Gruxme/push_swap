#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t lenght)
{
	size_t			i;
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	if (lenght == 0)
		return (0);
	while (i < lenght)
	{
		if (str1[i] != str2[i])
			return (str1[i] - str2[i]);
		i++;
	}
	return (0);
}
